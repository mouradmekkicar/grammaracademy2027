        "<p><b>Sequencing adverbs</b> (ordering connectors) signal the order of events or stages, which is essential when describing a process such as the steps of a rite of passage. At an advanced level you should command a wide range: to <b>open</b> — <i>initially, first(ly), to begin with</i>; to <b>continue</b> — <i>then, next, subsequently, after that, following this</i>; for <b>simultaneous</b> events — <i>meanwhile, simultaneously, at the same time</i>; to <b>close</b> — <i>eventually, finally, lastly, ultimately</i>.</p>",
        "<p>When a sequencing adverb opens a sentence, follow it with a <b>comma</b>: <i>Initially, the family gathers.</i> Distinguish <b>chronological</b> sequencing (time order) from <b>enumerative</b> sequencing (listing points: <i>firstly, secondly, finally</i>). Mind register too: <i>subsequently, thereafter</i> are formal, while <i>then, next</i> are neutral — and vary them rather than repeating <i>then</i>.</p>"
